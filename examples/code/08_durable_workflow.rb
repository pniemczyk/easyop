# ── Minimal AR stubs (no database) ────────────────────────────────────────────

class FakeScope
  include Enumerable

  def initialize(records) = @records = records
  def each(&blk)          = @records.each(&blk)
  def count               = @records.size
  def first               = @records.first
  def exists?             = @records.any?

  def where(*args, **conditions)
    return self if args.any? && conditions.empty?

    FakeScope.new(@records.select do |r|
      conditions.all? { |k, v| r.public_send(k) == v }
    end)
  end

  def update_all(**attrs)
    @records.each { |r| attrs.each { |k, v| r.public_send(:"#{k}=", v) } }
    @records.size
  end
end

class FakeFlowRun
  include Easyop::PersistentFlow::FlowRunModel

  attr_accessor :id, :flow_class, :context_data, :status, :current_step_index,
                :subject_type, :subject_id, :started_at, :finished_at

  @@store      = []
  @@id_counter = 0

  def self.store;   @@store;   end
  def self.reset!;  @@store = []; @@id_counter = 0; end

  def self.create!(attrs)
    obj = new
    attrs.each { |k, v| obj.public_send(:"#{k}=", v) }
    @@id_counter += 1
    obj.id = @@id_counter
    @@store << obj
    obj
  end

  def self.find(id)
    @@store.find { |r| r.id == id.to_i } or raise "FakeFlowRun #{id} not found"
  end

  def self.where(*args, **conditions)
    return FakeScope.new(@@store) if args.any? && conditions.empty?

    FakeScope.new(@@store.select do |r|
      conditions.all? { |k, v| r.public_send(k) == v }
    end)
  end

  def initialize
    @status             = 'pending'
    @current_step_index = 0
    @context_data       = '{}'
  end

  def update!(attrs)    = attrs.each { |k, v| public_send(:"#{k}=", v) } && self
  def update_columns(a) = update!(a)
  def reload            = self
end

