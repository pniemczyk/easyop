# ── Speedrun helper — drives all pending scheduled tasks synchronously ─────────

def speedrun(flow_run, max_iterations: 20)
  max_iterations.times do
    break if flow_run.terminal?
    # Find a pending task for this flow_run
    task = FakeScheduledTask.store.find do |t|
      t.state == 'scheduled' &&
        Easyop::Scheduler::Serializer.deserialize(t.ctx_data)[:flow_run_id] == flow_run.id
    end
    break unless task

    task.state = 'running'
    Easyop::PersistentFlow::Runner.execute_scheduled_step!(flow_run)
  end
  flow_run.reload
end

def reset_all!
  FakeFlowRun.reset!
  FakeFlowRunStep.reset!
  FakeScheduledTask.reset!
end

# ── Subject model — AR-compatible so Serializer can round-trip it ─────────────
# The Serializer stores AR objects as { __ar_class, __ar_id } and calls
# ClassName.find(id) on deserialization.

unless defined?(ActiveRecord)
  module ActiveRecord; class Base; end; end
end

class Subscriber < ActiveRecord::Base
  @@store = {}

  def self.store        = @@store
  def self.reset!       = (@@store = {})
  def self.find(id)     = @@store.fetch(id.to_i) { raise "Subscriber #{id} not found" }
  def self.create!(attrs)
    id = (@@store.keys.max || 0) + 1
    s  = new(id: id, **attrs)
    @@store[id] = s
    s
  end

  attr_accessor :id, :email, :active, :newsletter_opt_in

  def initialize(id: nil, email:, active:, newsletter_opt_in: true)
    @id              = id
    @email           = email
    @active          = active
    @newsletter_opt_in = newsletter_opt_in
    @@store[@id] = self if @id
  end
end

