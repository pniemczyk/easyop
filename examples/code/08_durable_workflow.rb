# ── Speedrun helper — drives all pending scheduled tasks synchronously ─────────

def speedrun(flow_run, max_iterations: 20)
  max_iterations.times do
    break if flow_run.terminal?
    # Find a pending task for this flow_run
    task = FakeScheduledTask.store.find do |t|
      t.state == 'scheduled' &&
        Easyop::Scheduler::Serializer.deserialize(t.ctx_data)[:flow_run_id] == flow_run.id
    end
    break unless task

    task.state = 'running'
    Easyop::PersistentFlow::Runner.execute_scheduled_step!(flow_run)
  end
  flow_run.reload
end

