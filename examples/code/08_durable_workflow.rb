# ── Durable flow definition ───────────────────────────────────────────────────

class ProcessSubscription
  include Easyop::Flow

  subject :subscriber   # triggers Mode 3 — .call returns FlowRun

  flow ValidateSubscription,
       ProcessPayment.on_exception(:reattempt!, max_reattempts: 2),
       ActivateAccount,
       SendWelcomeEmail.skip_if { |ctx| !ctx[:subscriber]&.newsletter_opt_in },
       NotifyBillingTeam
end

