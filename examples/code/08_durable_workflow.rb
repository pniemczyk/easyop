# 08 — Durable Flows (v0.5 unified API)
#
# Covers:
#   include Easyop::Flow + subject :user   — triggers durable mode (Mode 3)
#   subject stored as subject_type/subject_id on FlowRun row
#   flow steps run durable — ctx persisted across async boundaries
#   on_exception(:reattempt!, max_reattempts: N) — automatic retry
#   on_exception(:cancel!)                 — fail immediately on step error
#   flow_run.cancel!                       — cancel from outside the flow
#   flow_run.resume!                       — resume a paused flow
#   Free composition — outer plain flow embeds a durable inner → auto-promotes
#
# Note: uses in-memory FakeFlowRun/FakeFlowRunStep/FakeScheduledTask stubs
#       (no database needed). In a Rails app, use the EasyFlowRun/EasyFlowRunStep
#       AR models generated by `rails g easyop:install`.
#
# Run: ruby examples/code/08_durable_workflow.rb

