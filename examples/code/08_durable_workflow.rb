class DelayedFlow
  include Easyop::Flow
  plugin Easyop::Plugins::Async
  subject :subscriber
  flow ValidateSubscription,
       ActivateAccount.async(wait: 86_400),   # 1-day async step → pauses here
       NotifyBillingTeam
end

subscriber5 = Subscriber.create!(email: 'eve@example.com', active: true)
flow_run5 = DelayedFlow.call(subscriber: subscriber5)
# Runner.advance! ran ValidateSubscription, then hit the async ActivateAccount
# → status is 'running', a FakeScheduledTask was created.

puts "  status after .call:        #{flow_run5.status}"   # running
puts "  scheduled tasks pending:   #{FakeScheduledTask.store.count { |t| t.state == 'scheduled' }}"  # 1

flow_run5.cancel!

puts "  status after .cancel!:     #{flow_run5.status}"   # cancelled
puts "  scheduled tasks remaining: #{FakeScheduledTask.store.count { |t| t.state == 'scheduled' }}"  # 0

