class Subscriber < ActiveRecord::Base
  @@store = {}

  def self.store        = @@store
  def self.reset!       = (@@store = {})
  def self.find(id)     = @@store.fetch(id.to_i) { raise "Subscriber #{id} not found" }
  def self.create!(attrs)
    id = (@@store.keys.max || 0) + 1
    s  = new(id: id, **attrs)
    @@store[id] = s
    s
  end

  attr_accessor :id, :email, :active, :newsletter_opt_in

  def initialize(id: nil, email:, active:, newsletter_opt_in: true)
    @id              = id
    @email           = email
    @active          = active
    @newsletter_opt_in = newsletter_opt_in
    @@store[@id] = self if @id
  end
end

