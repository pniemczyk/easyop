class FakeScheduledTask
  attr_accessor :id, :operation_class, :ctx_data, :run_at, :tags, :state

  @@store      = []
  @@id_counter = 0

  def self.store;   @@store;   end
  def self.reset!;  @@store = []; @@id_counter = 0; end

  # Minimal connection stub so Scheduler._adapter_name does not raise
  def self.connection
    Struct.new(:adapter_name).new('fake_adapter')
  end

  def self.create!(attrs)
    obj = new
    attrs.each { |k, v| obj.public_send(:"#{k}=", v) if obj.respond_to?(:"#{k}=") }
    @@id_counter += 1
    obj.id    ||= @@id_counter
    obj.state ||= 'scheduled'
    @@store << obj
    obj
  end

  def self.find(id)   = @@store.find { |r| r.id == id }
  def self.where(*args, **conditions)
    return FakeScope.new(@@store) if args.any? && conditions.empty?

    FakeScope.new(@@store.select do |r|
      conditions.all? { |k, v| r.respond_to?(k) && r.public_send(k) == v }
    end)
  end

  def update_columns(attrs) = attrs.each { |k, v| public_send(:"#{k}=", v) if respond_to?(:"#{k}=") } && self

  def initialize
    @state = 'scheduled'
  end
end

