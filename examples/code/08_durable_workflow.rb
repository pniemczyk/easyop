# ── Step classes ──────────────────────────────────────────────────────────────

class ValidateSubscription
  include Easyop::Operation

  def call
    ctx.fail!(error: 'Subscriber is not active') unless ctx.subscriber&.active
    ctx.plan = ctx[:plan] || 'basic'
  end
end

class ProcessPayment
  include Easyop::Operation
  plugin Easyop::Plugins::Async   # step-builder DSL (on_exception) requires Async plugin

  @@fail_count = 0

  def self.reset_fail_count!   = (@@fail_count = 0)
  def self.set_failures(n)     = (@@fail_count = n)

  def call
    if @@fail_count > 0
      @@fail_count -= 1
      raise 'payment gateway timeout'
    end

    ctx.payment_id = "pay_#{SecureRandom.hex(6)}"
    puts "  [ProcessPayment] charged #{ctx.subscriber.email} → #{ctx.payment_id}"
  end
end

class ActivateAccount
  include Easyop::Operation
  plugin Easyop::Plugins::Async   # step-builder DSL (.async) requires Async plugin

  def call
    ctx.activated = true
    puts "  [ActivateAccount] account activated for #{ctx.subscriber.email}"
  end
end

class SendWelcomeEmail
  include Easyop::Operation
  plugin Easyop::Plugins::Async   # enables .skip_if fluent builder

  def call
